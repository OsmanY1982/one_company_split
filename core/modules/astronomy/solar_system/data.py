# ═══════════════════════════════════════════════════════
# 工具：轨道缩放
# ═══════════════════════════════════════════════════════
KM_SCALE = 0.00384   # sqrt(km) → px 缩放因子（zoom=1 时冥王星轨道 ≈ 325px）
ORBIT_BASE = 30       # 中心偏移（px）

def km_to_px(orbit_km, zoom=1.0):
    """轨道半径 km → 屏幕像素（sqrt 压缩）"""
    return ORBIT_BASE + math.sqrt(max(orbit_km, 1)) * KM_SCALE * zoom

def radius_to_px(radius_km, zoom=1.0):
    """天体半径 km → 屏幕像素（对数缩放，保证可见）"""
    if radius_km <= 0:
        return 1.0
    # 月球 1737km → ~3px @zoom=1, 木星 69911km → ~22px
    return max(0.8, math.log10(radius_km + 1) * 3.2 * zoom)

